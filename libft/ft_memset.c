#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	i = 0;
	tmp = (unsigned char *)s;
	while (i < n)
	{
		*(tmp) = (unsigned char)c;
		tmp++;
		i++;
	}
	return (s);
}
